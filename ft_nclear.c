#include "ft_minishell.h"

void	ft_nclear(size_t n)
{
	size_t		i;

	i = 0;
	while (i < n)
	{
		ft_putstr("\033[1D");
		ft_putstr(" ");
		ft_putstr("\033[1D");
		i++;
	}
}
